def db_scripts(config: ProjectConfig) -> dict[str, str]:
    if config.database not in {"mariadb", "postgresql"}:
        return {}
    is_mariadb = config.database == "mariadb"
    image = "docker.io/library/mariadb:latest" if is_mariadb else "docker.io/library/postgres:latest"
    name = f"agentkit-{project_id(config)}-{config.database}"
    volume = volume_name(config, "db_data")
    env_file = "--env-file .env.sandbox"
    port = "3306" if is_mariadb else "5432"
    data_path = "/var/lib/mysql" if is_mariadb else "/var/lib/postgresql/data"
    env_block = (
        "-e MARIADB_DATABASE=${SANDBOX_DB_NAME:-app_dev} \\\n"
        "  -e MARIADB_USER=${SANDBOX_DB_USER:-app_dev} \\\n"
        "  -e MARIADB_PASSWORD=${SANDBOX_DB_PASSWORD:-dev_password_change_me} \\\n"
        "  -e MARIADB_ROOT_PASSWORD=${SANDBOX_DB_PASSWORD:-dev_password_change_me}"
        if is_mariadb
        else "-e POSTGRES_DB=${SANDBOX_DB_NAME:-app_dev} \\\n"
        "  -e POSTGRES_USER=${SANDBOX_DB_USER:-app_dev} \\\n"
        "  -e POSTGRES_PASSWORD=${SANDBOX_DB_PASSWORD:-dev_password_change_me}"
    )
    up = clean(
        f"""
        #!/usr/bin/env sh
        set -eu
        {_host_side_guard()}
        test -f .env.sandbox || cp .env.sandbox.example .env.sandbox
        podman volume exists {shlex.quote(volume)} || podman volume create {shlex.quote(volume)}
        . ./.env.sandbox
        podman run -d --replace --name {shlex.quote(name)} --label agentkit.project={shlex.quote(project_id(config))} --label agentkit.generated=true {env_file} \\
          {env_block} \\
          -p 127.0.0.1:${{SANDBOX_DB_PORT:-{port}}}:{port} \\
          --volume {shlex.quote(volume)}:{data_path}:Z \\
          {shlex.quote(image)}
        """
    )
    return {
        "scripts/sandbox/db-up": up,
        "scripts/sandbox/db-down": "#!/usr/bin/env sh\nset -eu\n"
        + _host_side_guard()
        + f"\npodman stop {shlex.quote(name)} || true\n",
        "scripts/sandbox/db-shell": "#!/usr/bin/env sh\nset -eu\n"
        + _host_side_guard()
        + f"\npodman exec -it {shlex.quote(name)} /bin/sh\n",
        "scripts/sandbox/db-logs": "#!/usr/bin/env sh\nset -eu\n"
        + _host_side_guard()
        + f"\npodman logs -f {shlex.quote(name)}\n",
        ".env.sandbox.example": env_sandbox_example(config),
    }


