def project_id(config: ProjectConfig) -> str:
    slug = config.project_slug or "project"
    return "".join(character if character.isalnum() else "-" for character in slug.lower()).strip("-") or "project"


def image_name(config: ProjectConfig) -> str:
    return f"agentkit-{project_id(config)}:dev"


def volume_name(config: ProjectConfig, suffix: str) -> str:
    return f"agentkit_{project_id(config)}_{suffix}"


def sandbox_json(config: ProjectConfig) -> str:
    payload = {
        "schema_version": 1,
        "engine": "podman",
        "mode": config.sandbox.mode,
        "image_profile": config.sandbox.image_profile,
        "rootless_required": config.sandbox.rootless_required,
        "codex_inside_container": config.sandbox.codex_inside_container,
        "workspace_mount": "/workspace",
        "user_namespace": "keep-id",
        "runtime_user": "host uid/gid from id -u/id -g",
        "network_default": "none for toolchain check/exec/shell; build and explicit Codex container flows may use network",
        "image": image_name(config),
        "volumes": {
            "codex_home": volume_name(config, "codex_home"),
            "db_data": volume_name(config, "db_data"),
        },
        "project_local_paths": {
            "container_home": ".agent-starter/container-home",
            "cache": ".agent-starter/cache",
            "logs": ".agent-starter/logs",
        },
        "advanced_passthrough": {
            "gpu_audio_controller": config.sandbox.gui_passthrough,
        },
        "mount_policy": {
            "project_workspace": "rw",
            "host_home": "not-mounted",
            "host_codex_home": "not-mounted",
            "host_ssh": "not-mounted",
            "host_browser_profiles": "not-mounted",
            "host_git_credentials": "not-mounted",
        },
    }
    return json.dumps(payload, indent=2, sort_keys=True) + "\n"


def containerfile(config: ProjectConfig) -> str:
    capability_ids = capabilities_for(config.languages, config.database, github=False)
    if config.sandbox.codex_inside_container:
        capability_ids.append("language.javascript")
    if config.sandbox.image_profile == "debian-toolchain":
        packages = debian_packages_for_capabilities(capability_ids)
        base_image = "docker.io/library/debian:stable-slim"
        profile_note = "Debian toolchain image selected explicitly; this choice is independent of the host distribution."
        install_command = "apt-get update && apt-get install --yes --no-install-recommends {packages} && rm -rf /var/lib/apt/lists/*"
    else:
        packages = arch_packages_for_capabilities(capability_ids)
        base_image = "docker.io/library/archlinux:base-devel"
        profile_note = "Arch toolchain image selected explicitly; this choice is independent of the host distribution."
        install_command = "pacman -Syu --noconfirm --needed {packages} && pacman -Scc --noconfirm"
    install = " ".join(shlex.quote(package) for package in packages)
    codex_note = (
        "\n# Codex CLI is installed only for explicit codex-inside-container mode.\n"
        "RUN npm install -g @openai/codex\n"
        if config.sandbox.codex_inside_container
        else ""
    )
    return clean(
        f"""
        # Generated by CLI AI Agent Starter Kit.
        # {profile_note}
        FROM {base_image}

        RUN {install_command.format(packages=install)}
        RUN mkdir -p /workspace /home/codex /tmp/agentkit-home \\
            && chmod 1777 /tmp/agentkit-home \\
            && chmod 0755 /home/codex
        {codex_note}
        WORKDIR /workspace
        """
    )


def _inside_sandbox_helper() -> str:
    return clean(
        """
        inside_agentkit_sandbox() {
          test "${AGENTKIT_INSIDE_SANDBOX:-}" = "1" && return 0
          test -f /run/.containerenv && return 0
          test -f /.dockerenv && return 0
          return 1
        }
        """
    )


def _host_side_guard() -> str:
    return (
        _inside_sandbox_helper()
        + "\n"
        + clean(
            f"""
            if inside_agentkit_sandbox; then
              printf '%s\\n' {shlex.quote(HOST_SIDE_SANDBOX_MESSAGE)}
              exit 2
            fi
            """
        )
    )


def _script_prelude(config: ProjectConfig) -> str:
    return clean(
        f"""
        #!/usr/bin/env sh
        set -eu
        ROOT=$(CDPATH= cd -- "$(dirname -- "$0")/../.." && pwd)
        IMAGE={shlex.quote(image_name(config))}
        WORKSPACE=/workspace
        CACHE_VOLUME={shlex.quote(volume_name(config, "cache"))}
        CODEX_HOME_VOLUME={shlex.quote(volume_name(config, "codex_home"))}
        DB_VOLUME={shlex.quote(volume_name(config, "db_data"))}
        HOST_UID=$(id -u)
        HOST_GID=$(id -g)
        CONTAINER_HOME=/tmp/agentkit-home
        CONTAINER_CACHE=/tmp/agentkit-cache
        HOST_CONTAINER_HOME="$ROOT/.agent-starter/container-home"
        HOST_CACHE="$ROOT/.agent-starter/cache"
        LOG_DIR="$ROOT/.agent-starter/logs"
        SANDBOX_NETWORK=${{AGENTKIT_SANDBOX_NETWORK:-none}}

        {_inside_sandbox_helper()}

        prepare_project_container_dirs() {{
          mkdir -p "$HOST_CONTAINER_HOME" "$HOST_CACHE" "$LOG_DIR"
        }}

        run_project_container() {{
          prepare_project_container_dirs
          podman run --rm \\
            --label agentkit.project={shlex.quote(project_id(config))} \\
            --label agentkit.generated=true \\
            --userns=keep-id \\
            --user "$HOST_UID:$HOST_GID" \\
            --network "$SANDBOX_NETWORK" \\
            --security-opt=no-new-privileges \\
            --cap-drop=all \\
            --pids-limit "${{AGENTKIT_SANDBOX_PIDS_LIMIT:-512}}" \\
            --env AGENTKIT_INSIDE_SANDBOX=1 \\
            --env HOME="$CONTAINER_HOME" \\
            --env XDG_CACHE_HOME="$CONTAINER_CACHE/xdg-cache" \\
            --env XDG_CONFIG_HOME="$CONTAINER_HOME/.config" \\
            --env NPM_CONFIG_CACHE="$CONTAINER_CACHE/npm" \\
            --env YARN_CACHE_FOLDER="$CONTAINER_CACHE/yarn" \\
            --env PNPM_HOME="$CONTAINER_CACHE/pnpm-home" \\
            --env PIP_CACHE_DIR="$CONTAINER_CACHE/pip" \\
            --env COMPOSER_CACHE_DIR="$CONTAINER_CACHE/composer" \\
            --env CARGO_HOME="$CONTAINER_CACHE/cargo" \\
            --env GOPATH="$CONTAINER_CACHE/go" \\
            --volume "$ROOT:/workspace:Z" \\
            --volume "$HOST_CONTAINER_HOME:$CONTAINER_HOME:Z" \\
            --volume "$HOST_CACHE:$CONTAINER_CACHE:Z" \\
            --workdir /workspace \\
            "$IMAGE" "$@"
        }}
        """
    )


def doctor_script(config: ProjectConfig) -> str:
    return clean(
        f"""
        #!/usr/bin/env sh
        set -eu
        ROOT=$(CDPATH= cd -- "$(dirname -- "$0")/../.." && pwd)
        {_host_side_guard()}
        printf '%s\\n' 'Agent Kit rootless Podman sandbox doctor'
        if ! command -v podman >/dev/null 2>&1; then
          printf '%s\\n' '[missing] podman'
          printf 'Review on CachyOS: %s\\n' {shlex.quote(CACHYOS_PODMAN_COMMAND)}
          exit 2
        fi
        podman info >/dev/null
        if podman info --format '{{{{.Host.Security.Rootless}}}}' 2>/dev/null | grep -qi true; then
          printf '%s\\n' '[ok] Podman reports rootless mode.'
        else
          printf '%s\\n' '[warn] Could not confirm rootless mode from podman info.'
        fi
        USER_NAME=$(id -un)
        printf '[ok] host user id: %s:%s\\n' "$(id -u)" "$(id -g)"
        grep -q "^${{USER_NAME}}:" /etc/subuid 2>/dev/null && printf '%s\\n' '[ok] /etc/subuid has a mapping for this user.' || printf '%s\\n' '[warn] /etc/subuid mapping not found or not inspectable.'
        grep -q "^${{USER_NAME}}:" /etc/subgid 2>/dev/null && printf '%s\\n' '[ok] /etc/subgid has a mapping for this user.' || printf '%s\\n' '[warn] /etc/subgid mapping not found or not inspectable.'
        if podman run --help 2>/dev/null | grep -q -- '--userns'; then
          printf '%s\\n' '[ok] Podman supports --userns=keep-id for host-owned workspace files.'
        else
          printf '%s\\n' '[warn] Could not confirm Podman --userns support; workspace file ownership may need review.'
        fi
        command -v pasta >/dev/null 2>&1 || command -v passt >/dev/null 2>&1 || printf '%s\\n' '[warn] passt/pasta not found; rootless networking may be limited.'
        command -v fuse-overlayfs >/dev/null 2>&1 || printf '%s\\n' '[warn] fuse-overlayfs not found; Podman may use another storage driver.'
        test -f "$ROOT/.agent-starter/sandbox/sandbox.json" && printf '%s\\n' '[ok] sandbox config exists.'
        podman image exists {shlex.quote(image_name(config))} && printf '%s\\n' '[ok] sandbox image exists.' || printf '%s\\n' '[info] sandbox image needs build: scripts/sandbox/build'
        """
    )


